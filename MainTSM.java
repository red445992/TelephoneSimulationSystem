    int from, to;
    int arrival;
    int length;
    int end;
    int processed = 0;  // Start from 0 for accurate counting
    int completed = 0;  // Start from 0 for accurate counting
    int blocke = 0;     // Start from 0 for accurate counting
    int busy = 0;       // Start from 0 for accurate counting
    int clock = 0;
    int max = 3, inuse = 0, stat;
    int used[][] = new int[3][3];
    
    // Enhanced statistics for Erlang-B
    int totalCalls = 0;

    public void inite() {
        String fromt, tot, lengtht, arrivalt;
        try {
            fromt = jTextField9.getText().trim();
            tot = jTextField10.getText().trim();
            lengtht = jTextField12.getText().trim();
            arrivalt = jTextField14.getText().trim();
            
            // Validate that all fields are not empty
            if (fromt.isEmpty() || tot.isEmpty() || lengtht.isEmpty() || arrivalt.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Please fill in all fields (From Line, To Line, Call Length, Arrival Time)", 
                    "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Parse the values
            from = Integer.parseInt(fromt);
            to = Integer.parseInt(tot);
            length = Integer.parseInt(lengtht);
            arrival = Integer.parseInt(arrivalt);
            
            // Validate ranges
            if (from < 1 || from > 8 || to < 1 || to > 8) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "From Line and To Line must be between 1 and 8", 
                    "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            if (length <= 0 || arrival < 0) {
                javax.swing.JOptionPane.showMessageDialog(this, 
                    "Call Length must be positive and Arrival Time must be non-negative", 
                    "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
                return;
            }
            
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Please enter valid integers in all fields", 
                "Input Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Enhanced call processing
        totalCalls++;
        clock = arrival;
        callcompletecheck();
        stat = callstat();
        jTextField28.setText(String.valueOf(clock));
        
        if (stat == 0) {
            end = arrival + length;
            connection();
            used[inuse][0] = from;
            used[inuse][1] = to;
            used[inuse][2] = end;
            inuse++;
            completed++;
            processed++;
        } else {
            processed++;
        }
        
        putData();
        repaint();
    }
    
    // Method to get enhanced statistics
    public String getStatistics() {
        double blockingRate = totalCalls > 0 ? (double) blocke / totalCalls * 100 : 0;
        double busyRate = totalCalls > 0 ? (double) busy / totalCalls * 100 : 0;
        double completionRate = totalCalls > 0 ? (double) completed / totalCalls * 100 : 0;
        
        return String.format("Enhanced Erlang-B Statistics:\n" +
                           "Total Calls: %d\n" +
                           "Completed Calls: %d\n" +
                           "Blocked Calls: %d\n" +
                           "Busy Calls: %d\n" +
                           "Completion Rate: %.1f%%\n" +
                           "Blocking Rate: %.1f%%\n" +
                           "Busy Rate: %.1f%%",
                           totalCalls, completed, blocke, busy,
                           completionRate, blockingRate, busyRate);
    }

