    public void callcompletecheck() {
        int i;
        for (i = 0; i < inuse; i++) {
            if (used[i][2] <= arrival && used[i][2] > 0) {
                completed++;
                processed++;
                clock = used[i][2];
                int fromLine = used[i][0] - 1;
                int toLine = used[i][1] - 1;
                line[fromLine] = 0;
                line[toLine] = 0;
                for (int j = i; j < inuse - 1; j++) {
                    used[j][0] = used[j + 1][0];
                    used[j][1] = used[j + 1][1];
                    used[j][2] = used[j + 1][2];
                }
                used[inuse - 1][0] = used[inuse - 1][1] = used[inuse - 1][2] = 0;
                inuse--;
                i--;
            } else
                clock = arrival;
        }
    }

    public int callstat() {
        int stat = 0;
        if (line[from - 1] == 1 || line[to - 1] == 1) {
            busy++;
            processed++;
            stat = 1;
            clock = arrival;
            return stat;
        }
        if (inuse == 3) {
            blocke++;
            processed++;
            clock = arrival;
            stat = 2;
            return stat;
        } else
            return 0;
    }

